func search(nums []int, target int) bool {
	if len(nums) == 0 {
		return false
	}
	return basicSearch(nums, 0, len(nums)-1, target)
}

func basicSearch(nums []int, begin int, end int, target int) bool {
	middle := (begin + end) / 2
	if nums[middle] == target {
		return true
	}

	found := false
	if begin <= middle-1 {
		if nums[begin] < nums[middle-1] {
			found = binarySearch(nums, begin, middle-1, target)
		} else {
			found = basicSearch(nums, begin, middle-1, target)
		}
		if found {
			return true
		}
	}

	if middle+1 <= end {
		if nums[middle+1] < nums[end] {
			found = binarySearch(nums, middle+1, end, target)
		} else {
			found = basicSearch(nums, middle+1, end, target)
		}
	}

	return found
}

func binarySearch(nums []int, begin int, end int, target int) bool {
	for begin <= end {
		middle := (begin + end) / 2
		if nums[middle] == target {
			return true
		} else if nums[middle] > target {
			end = middle - 1
		} else {
			begin = middle + 1
		}
	}

	return false
}
